    public void eliminarRegistros(){
        int filaSeleccionada = tblClientes.getSelectedRow();
        
        try {
            String SQL = "delete from usuarios where idusuarios="+tblClientes.getValueAt(filaSeleccionada,0);
            
            Statement st = con.createStatement();
            
            int n = st.executeUpdate(SQL);
            
            if (n>=0) {
                
                JOptionPane.showMessageDialog(null, "Registro Eliminado");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al Eliminar registro, debe seleccionar un campo"+e.getMessage());
        }
        
    }
    
    
    
    public void mostrarDatos(){
        
        String[] titulos={"ID","Nombre_Usuario","Nombre","Apellido","Numero_Telefono","Email"};
        String[] registros = new String[6];
        
        DefaultTableModel modelo = new DefaultTableModel(null,titulos);
        
        String SQL = "select * from usuarios";
        
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            
            
            
            while (rs.next()) {                
                
                registros[0]=rs.getString("idusuarios");
                registros[1]=rs.getString("NombreUsuario");
                registros[2]=rs.getString("Nombre");
                registros[3]=rs.getString("Apellido");
                registros[4]=rs.getString("Telefono");
                registros[5]=rs.getString("Email");
                
                modelo.addRow(registros);
                
            }
            
            tblClientes.setModel(modelo);
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "Error al cargar los datos"+e);
        }
    }
