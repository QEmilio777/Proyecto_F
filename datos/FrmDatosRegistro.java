    public void enviarDatosVentana(){
        if (tblClientes.getSelectedRow()>=0) {
        FrmDetallesUsuario abrir = new FrmDetallesUsuario();
        abrir.setVisible(true);
            try {
                DefaultTableModel tm = (DefaultTableModel)tblClientes.getModel();
                
                int ID1 = Integer.parseInt(String.valueOf(tm.getValueAt(tblClientes.getSelectedRow(),0)));
                String nombreUsuario = String.valueOf(tm.getValueAt(tblClientes.getSelectedRow(), 1));
                String nombre = String.valueOf(tm.getValueAt(tblClientes.getSelectedRow(), 2));
                String apellido = String.valueOf(tm.getValueAt(tblClientes.getSelectedRow(), 3));
                String telefono = String.valueOf(tm.getValueAt(tblClientes.getSelectedRow(), 4));
                String email = String.valueOf(tm.getValueAt(tblClientes.getSelectedRow(), 5));
                
                FrmDetallesUsuario.txt_nombre.setText(nombre);
                FrmDetallesUsuario.txt_usuario.setText(nombreUsuario);
                FrmDetallesUsuario.txt_telefono.setText(telefono);
                FrmDetallesUsuario.txt_apellido.setText(apellido);
                FrmDetallesUsuario.txt_email.setText(email);
                FrmDetallesUsuario.txt_ID.setText(String.valueOf(ID1));
                this.setVisible(false);
                con.close();
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "No contiene un producto");
                
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un producto","Sistema",JOptionPane.WARNING_MESSAGE);
        }
    }
    
