    public void mostrarDatos(){
        
        String[] titulos={"ID","Nombre_Producto","Marca","Categoría","Precio","Cantidad_Disponible"};
        String[] registros = new String[6];
        
        DefaultTableModel modelo = new DefaultTableModel(null,titulos);
        
        String SQL = "select * from productos";
        
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            
            
            
            while (rs.next()) {                
                
                registros[0]=rs.getString("idProducto");
                registros[1]=rs.getString("NombreProducto");
                registros[2]=rs.getString("MarcaProducto");
                registros[3]=rs.getString("CategoriaProducto");
                registros[4]=rs.getString("PrecioProducto");
                registros[5]=rs.getString("StockProducto");
                
                modelo.addRow(registros);
                
            }
            
            tblProductos.setModel(modelo);
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "Error al cargar los datos"+e);
        }
    }
