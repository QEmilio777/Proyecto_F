    public void enviarDatosVentana(){
        if (tblProductos.getSelectedRow()>=0) {
        FrmActualizarProducto abrir = new FrmActualizarProducto();
        abrir.setVisible(true);
            try {
                DefaultTableModel tm = (DefaultTableModel)tblProductos.getModel();
                
                int ID1 = Integer.parseInt(String.valueOf(tm.getValueAt(tblProductos.getSelectedRow(),0)));
                String nombreProducto = String.valueOf(tm.getValueAt(tblProductos.getSelectedRow(), 1));
                String marca = String.valueOf(tm.getValueAt(tblProductos.getSelectedRow(), 2));
                String categoria = String.valueOf(tm.getValueAt(tblProductos.getSelectedRow(), 3));
                String precio = String.valueOf(tm.getValueAt(tblProductos.getSelectedRow(), 4));
                String stock = String.valueOf(tm.getValueAt(tblProductos.getSelectedRow(), 5));
                
                FrmActualizarProducto.txt_nombreProducto.setText(nombreProducto);
                FrmActualizarProducto.txt_marcaProducto.setText(marca);
                FrmActualizarProducto.txt_categoriaProducto.setText(categoria);
                FrmActualizarProducto.txt_precio.setText(precio);
                FrmActualizarProducto.txt_cantidadDisponible.setText(stock);
                FrmActualizarProducto.txt_IDP.setText(String.valueOf(ID1));
                this.setVisible(false);
                con.close();
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "No contiene un producto");
                
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un producto","Sistema",JOptionPane.WARNING_MESSAGE);
        }
    }
    
